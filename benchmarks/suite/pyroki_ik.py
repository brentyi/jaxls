"""A/B regression benchmark: pyroki IK-Beam on two jaxls versions.

The pyroki side of /tmp/pyroki/benchmark/ik_benchmark.py (cuRobo comparison
and torch removed; targets generated by FK on uniformly sampled joint
configurations, same recipe the original uses for its q_sample). Select the
jaxls under test via PYTHONPATH:

    python /tmp/pyroki_ab.py --label branch
    PYTHONPATH=/tmp/jaxls-main/src python /tmp/pyroki_ab.py --label main
"""

import argparse
import json
import time
import xml.etree.ElementTree as ET
from io import StringIO

import jax
import jaxlie
import numpy as np
import yourdfpy
from jax import lax
from jax import numpy as jnp
from robot_descriptions.loaders.yourdfpy import load_robot_description

import jaxls
import pyroki as pk  # type: ignore[import-not-found]


def newton_raphson(f, x, iters):
    def update(x, _):
        y = x - f(x) / jax.grad(f)(x)
        return y, None

    x, _ = lax.scan(update, 1.0, length=iters)
    return x


def roberts_sequence(num_points, dim, root):
    basis = 1 - (1 / root ** (1 + jnp.arange(dim)))
    n = jnp.arange(num_points)
    x = n[:, None] * basis[None, :]
    x, _ = jnp.modf(x)
    return x


class PyrokiIkBeamHelper:
    """Verbatim from pyroki/benchmark/ik_benchmark.py."""

    def __init__(self):
        urdf = load_robot_description("panda_description")
        xml_tree = urdf.write_xml()
        for joint in xml_tree.findall('.//joint[@type="prismatic"]'):
            joint.set("type", "fixed")
            for tag in ("axis", "limit", "dynamics"):
                child = joint.find(tag)
                if child is not None:
                    joint.remove(child)
        xml_str = ET.tostring(xml_tree.getroot(), encoding="unicode")
        urdf = yourdfpy.URDF.load(StringIO(xml_str))
        assert urdf.validate()

        robot = pk.Robot.from_urdf(urdf)
        ee_link_name = "panda_hand_tcp"
        self.target_link_index = jnp.array(robot.links.names.index(ee_link_name))
        self.robot = robot
        exp = robot.joints.num_actuated_joints
        self.root = newton_raphson(lambda x: x ** (exp + 1) - x - 1, 1.0, 10_000)

    def solve_ik(self, target_wxyz: jax.Array, target_position: jax.Array) -> jax.Array:
        num_seeds_init: int = 64
        num_seeds_final: int = 4
        total_steps: int = 16
        init_steps: int = 6

        def solve_one(initial_q, lambda_initial, max_iters):
            joint_var = robot.joint_var_cls(0)
            factors = [
                pk.costs.pose_cost_analytic_jac(
                    robot,
                    joint_var,
                    jaxlie.SE3.from_rotation_and_translation(
                        jaxlie.SO3(target_wxyz), target_position
                    ),
                    self.target_link_index,
                    pos_weight=10.0,
                    ori_weight=5.0,
                ),
                pk.costs.limit_cost(robot, joint_var, weight=50.0),
            ]
            sol, summary = (
                jaxls.LeastSquaresProblem(factors, [joint_var])
                .analyze()
                .solve(
                    initial_vals=jaxls.VarValues.make(
                        [joint_var.with_value(initial_q)]
                    ),
                    verbose=False,
                    linear_solver="dense_cholesky",
                    termination=jaxls.TerminationConfig(
                        max_iterations=max_iters,
                        early_termination=False,
                    ),
                    trust_region=jaxls.TrustRegionConfig(lambda_initial=lambda_initial),
                    return_summary=True,
                )
            )
            return sol[joint_var], summary

        vmapped_solve = jax.vmap(solve_one, in_axes=(0, 0, None))
        robot = self.robot
        initial_qs = robot.joints.lower_limits + roberts_sequence(
            num_seeds_init, robot.joints.num_actuated_joints, self.root
        ) * (robot.joints.upper_limits - robot.joints.lower_limits)

        initial_sols, summary = vmapped_solve(
            initial_qs, jnp.full(initial_qs.shape[:1], 10.0), init_steps
        )
        best_initial_sols = jnp.argsort(
            summary.cost_history[jnp.arange(num_seeds_init), -1]
        )[:num_seeds_final]
        best_sols, summary = vmapped_solve(
            initial_sols[best_initial_sols],
            summary.lambda_history[jnp.arange(num_seeds_init), -1][best_initial_sols],
            total_steps - init_steps,
        )
        return best_sols[
            jnp.argmin(
                summary.cost_history[jnp.arange(num_seeds_final), summary.iterations]
            )
        ]

    def forward_kinematics(self, q):
        return self.robot.forward_kinematics(jnp.asarray(q))[self.target_link_index]


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--label", default="suite")
    ap.add_argument(
        "--batch-sizes", type=int, nargs="+", default=[1, 10, 100, 1000, 2000]
    )
    ap.add_argument("--out", default=None)
    args = ap.parse_args()

    print(f"jaxls under test: {jaxls.__file__}")

    helper = PyrokiIkBeamHelper()
    batched_ik = jax.jit(jax.vmap(helper.solve_ik))
    batched_fk = jax.jit(jax.vmap(helper.forward_kinematics))

    rng = np.random.default_rng(2)  # Same role as the benchmark's torch seed 2.
    lo = np.asarray(helper.robot.joints.lower_limits)
    hi = np.asarray(helper.robot.joints.upper_limits)

    results = {"label": args.label, "jaxls": jaxls.__file__, "rows": []}
    for b in args.batch_sizes:
        q_sample = rng.uniform(lo, hi, size=(b, lo.shape[0]))
        target = np.asarray(batched_fk(q_sample))
        wxyz, pos = jnp.asarray(target[..., 0:4]), jnp.asarray(target[..., 4:7])

        # Warmup (compile), then min of 3.
        sols = jax.block_until_ready(batched_ik(wxyz, pos))
        times = []
        for _ in range(3):
            t0 = time.perf_counter()
            sols = jax.block_until_ready(batched_ik(wxyz, pos))
            times.append(time.perf_counter() - t0)

        # Errors of the solved poses vs targets.
        reached = np.asarray(batched_fk(np.asarray(sols)))
        pos_err = np.linalg.norm(reached[..., 4:7] - target[..., 4:7], axis=-1)
        ori_err = np.asarray(
            jnp.abs(
                (
                    jaxlie.SO3(jnp.asarray(reached[..., 0:4])).inverse()
                    @ jaxlie.SO3(jnp.asarray(target[..., 0:4]))
                ).log()
            ).max(axis=-1)
        )
        succ = float(np.mean((pos_err < 1e-3) & (ori_err < 1e-3)) * 100)
        row = {
            "batch": b,
            "time_ms": min(times) * 1e3,
            "succ_pct": succ,
            "pos_err_mm_mean": float(pos_err.mean() * 1e3),
            "pos_err_mm_p99": float(np.quantile(pos_err, 0.99) * 1e3),
            "ori_err_mean": float(ori_err.mean()),
        }
        results["rows"].append(row)
        print(
            f"batch={b:>5}  {row['time_ms']:>9.2f} ms  succ={succ:6.2f}%  "
            f"pos_err mean={row['pos_err_mm_mean']:.5f} mm p99={row['pos_err_mm_p99']:.5f} mm  "
            f"ori_err mean={row['ori_err_mean']:.2e}",
            flush=True,
        )

    import json as _json

    print("__PYROKI__" + _json.dumps(results["rows"]) + "__END__", flush=True)
    out = args.out or f"/tmp/pyroki_ab_{args.label}.json"
    with open(out, "w") as f:
        json.dump(results, f, indent=2)
    print("wrote", out)


if __name__ == "__main__":
    main()
