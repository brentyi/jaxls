"""Assemble benchmarks/regression.md from regression-suite result JSONs.

Inputs (written by the regression suite; see results.md "Reproduce"):
    results/regression/nb_main.json, nb_final.json      notebook sweeps
    results/regression/examples.json                    examples/*.py sweep
    results/regression/pyroki_main.json, pyroki_final.json (optional)
    results/device_{toy,ladybug49,trafalgar138}_tuned_{main,final}.json
        BA feature sweep at lambda_initial=1e2 (uniform across methods).

Regenerate with: uv run --extra dev --extra docs python benchmarks/make_regression_report.py
"""

from __future__ import annotations

import json
from pathlib import Path

RESULTS = Path(__file__).parent / "results"
REG = RESULTS / "regression"
OUT = Path(__file__).parent / "regression.md"


def _load(path: Path):
    return json.loads(path.read_text()) if path.exists() else None


def _fmt(x, spec=".6g"):
    if x is None or x != x:
        return "—"
    return format(x, spec)


def notebook_table() -> list[str]:
    main = {r["notebook"]: r for r in _load(REG / "nb_main.json") or []}
    fin = {r["notebook"]: r for r in _load(REG / "nb_final.json") or []}
    lines = [
        "| notebook | cost (main) | cost (PR) | iters (main/PR) | solve time (main/PR) |",
        "|---|---:|---:|---:|---:|",
    ]
    for n in sorted(set(main) | set(fin)):
        m, f = main.get(n), fin.get(n)

        def g(r, k):
            return r.get(k) if r and r.get("success") else None

        lines.append(
            f"| {n} | {_fmt(g(m, 'final_cost'))} | {_fmt(g(f, 'final_cost'))} "
            f"| {_fmt(g(m, 'iterations'), '.0f')}/{_fmt(g(f, 'iterations'), '.0f')} "
            f"| {_fmt(g(m, 'solve_time_s'), '.3f')}s/{_fmt(g(f, 'solve_time_s'), '.3f')}s |"
        )
    return lines


def examples_table() -> list[str]:
    data = _load(REG / "examples.json") or {}
    lines = [
        "| example | cost (main) | cost (PR) | iters (main/PR) | solve time (main/PR) |",
        "|---|---:|---:|---:|---:|",
    ]
    for name, cfgs in sorted(data.items()):
        m = (cfgs.get("main") or {}).get("result")
        f = (cfgs.get("final") or {}).get("result")

        def g(r, k):
            return r.get(k) if r else None

        lines.append(
            f"| {name} | {_fmt(g(m, 'final_cost'))} | {_fmt(g(f, 'final_cost'))} "
            f"| {_fmt(g(m, 'iterations'), '.0f')}/{_fmt(g(f, 'iterations'), '.0f')} "
            f"| {_fmt(g(m, 'solve_time_s'), '.3f')}s/{_fmt(g(f, 'solve_time_s'), '.3f')}s |"
        )
    return lines


def ba_table() -> list[str]:
    lines = []
    for prob in ["toy", "ladybug49", "trafalgar138"]:
        main = _load(RESULTS / f"device_{prob}_tuned_main.json")
        fin = _load(RESULTS / f"device_{prob}_tuned_final.json")
        either = fin or main
        if either is None:
            continue
        title = either["title"]
        lines += [
            f"\n### {title}",
            "",
            "Accepted cost / wall-clock at the largest k run "
            "(float64, lambda_initial=1e2 for every method):",
            "",
            "| version | method | device | k | cost | time |",
            "|---|---|---|---:|---:|---:|",
        ]
        for label, data in [("main", main), ("PR", fin)]:
            if data is None:
                continue
            for run in data["runs"].values():
                ks = run.get("ks", data["ks"])
                lines.append(
                    f"| {label} | {run['method']} | {run['device']} | {ks[-1]} "
                    f"| {run['costs'][-1]:,.6g} | {run['times'][-1]:.3f} s |"
                )
    return lines


def main() -> None:
    parts = [
        "# Regression report: jaxls main vs this PR",
        "",
        "Generated by `benchmarks/make_regression_report.py` from the JSONs in",
        "`benchmarks/results/regression/` and `benchmarks/results/`. All runs:",
        "one warmup solve (absorbs compilation), then timed jit-cached solves.",
        "Library defaults unless stated; notebook/example rows use each",
        "problem's own solver settings, exactly as a user would run them.",
        "",
        "Known benign deltas: the two cart-pole notebooks compare",
        "non-converged snapshots (every config hits the iteration cap on a",
        "non-convex problem; gaps shrink to basin-level noise at higher",
        "caps), and near-threshold iteration counts can differ across",
        "devices (e.g. cam_design terminates at 2 iterations on CPU under",
        "both versions; GPU float reductions land its final cost-delta just",
        "above the 1e-6 tolerance).",
        "",
        "## Example notebooks (docs/source/examples)",
        "",
        *notebook_table(),
        "",
        "## examples/*.py",
        "",
        *examples_table(),
        "",
        "## Bundle adjustment (the headline feature)",
        "",
        "jaxls@main has no variable elimination: its best options are full-",
        "system CG or cholmod. The PR adds Schur+dense and Schur+CG.",
        *ba_table(),
        "",
    ]
    pyroki_m = _load(REG / "pyroki_main.json")
    pyroki_f = _load(REG / "pyroki_final.json")
    if pyroki_m and pyroki_f:
        parts += [
            "## pyroki IK-Beam (downstream)",
            "",
            "| batch | time main | time PR | succ main | succ PR | p99 err main | p99 err PR |",
            "|---:|---:|---:|---:|---:|---:|---:|",
        ]
        for rm, rf in zip(pyroki_m["rows"], pyroki_f["rows"]):
            parts.append(
                f"| {rm['batch']} | {rm['time_ms']:.1f} ms | {rf['time_ms']:.1f} ms "
                f"| {rm['succ_pct']:.2f}% | {rf['succ_pct']:.2f}% "
                f"| {rm['pos_err_mm_p99']:.3f} mm | {rf['pos_err_mm_p99']:.3f} mm |"
            )
        parts.append("")
    OUT.write_text("\n".join(parts))
    print(f"wrote {OUT}")


if __name__ == "__main__":
    main()
